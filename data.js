// NVI color scale
function nviColor(nvi) {
  if (nvi >= 80) return "#ef4444";
  if (nvi >= 65) return "#f97316";
  if (nvi >= 50) return "#f59e0b";
  if (nvi >= 35) return "#84cc16";
  return "#22c55e";
}

function nviLabel(nvi) {
  if (nvi >= 80) return "Critical";
  if (nvi >= 65) return "High";
  if (nvi >= 50) return "Moderate";
  if (nvi >= 35) return "Low-Moderate";
  return "Low";
}

